x = 14
y = 4
print('x+y =', x+y )  # soma
print('x+y=' , x+y )  # soma
print("x-y =", x-y )  # subtracao
print('x*y =', x*y )  # multiplicacao
print('x/y =', x/y )  # divisao
print('x//y=', x//y)  # parte inteira divisao
print('x%y =', x%y )  # resto da divisao
print('x**2=', x**2)  # Exponenciacao
print('raiz 4=', 4**(1/2) )  # raiz quadrada
print(f'A soma de {x} + {y} = {x+y}')  # exibindo com f-string


